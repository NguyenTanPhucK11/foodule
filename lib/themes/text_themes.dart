class TextThemeApp {
  static TextTheme primaryTextTheme = TextTheme(
    headline2: StylesText.headline2,
    headline3: StylesText.headline3,
    headline4: StylesText.headline4,
    headline5: StylesText.headline5,
    headline6: StylesText.headline6,
    bodyText1: StylesText.bodyText1,
    bodyText2: StylesText.bodyText2,
    caption: StylesText.caption,
    // ...
  ).apply(
    displayColor: AppColors.primaryOrangeRed,
    bodyColor: AppColors.primaryOrangeRed,
  );

  static TextTheme accentTextTheme = primaryTextTheme.copyWith().apply(
        displayColor: AppColors.neutral1,
      );
}
